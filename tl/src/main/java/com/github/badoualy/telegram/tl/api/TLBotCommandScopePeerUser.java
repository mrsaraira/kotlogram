import java.lang.SuppressWarnings;

public class TLBotCommandScopePeerUser extends TLAbsBotCommandScope {
    public static final int CONSTRUCTOR_ID = 0xa1321f3;

    protected TLAbsInputPeer peer;

    protected TLAbsInputUser userId;

    private final String _constructor = "botCommandScopePeerUser#a1321f3";

    public TLBotCommandScopePeerUser() {
    }

    public TLBotCommandScopePeerUser(TLAbsInputPeer peer, TLAbsInputUser userId) {
        this.peer = peer;
        this.userId = userId;
    }

    @Override
    public void serializeBody(OutputStream stream) throws IOException {
        writeTLObject(peer, stream);
        writeTLObject(userId, stream);
    }

    @Override
    @SuppressWarnings({"unchecked", "SimplifiableConditionalExpression"})
    public void deserializeBody(InputStream stream, TLContext context) throws IOException {
        peer = readTLObject(stream, context, TLAbsInputPeer.class, -1);
        userId = readTLObject(stream, context, TLAbsInputUser.class, -1);
    }

    @Override
    public int computeSerializedSize() {
        int size = SIZE_CONSTRUCTOR_ID;
        size += peer.computeSerializedSize();
        size += userId.computeSerializedSize();
        return size;
    }

    @Override
    public String toString() {
        return _constructor;
    }

    @Override
    public int getConstructorId() {
        return CONSTRUCTOR_ID;
    }

    public TLAbsInputPeer getPeer() {
        return peer;
    }

    public void setPeer(TLAbsInputPeer peer) {
        this.peer = peer;
    }

    public TLAbsInputUser getUserId() {
        return userId;
    }

    public void setUserId(TLAbsInputUser userId) {
        this.userId = userId;
    }
}
